import argparse
from utils.analyze_utils import analyze_incorrect_ans

if __name__ == "__main__":
    """
    Classifies and analyzes incorrect answers generated by a model, and saves the results to a specified directory.
    """
    parser = argparse.ArgumentParser()

    parser.add_argument("--model_name", type=str, default="gpt-4o")

    parser.add_argument("--api_key", type=str)

    parser.add_argument("--data_dir", type=str,
                        help="The parent dir of dataset ")

    parser.add_argument("--save_dir", type=str, default="./statistics",
                        help="The local dir to save the analyze result")

    args = parser.parse_args()

    analyze_incorrect_ans(args.model_name, args.api_key,
                          args.data_dir, args.save_dir)
